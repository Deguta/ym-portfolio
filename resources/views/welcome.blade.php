              <b>【柔軟な発想での課題解決力】コロナ不況による売上低迷を打破</b><br>
                現職では機械工具のルート営業をしています。新型コロナにより設備投資がなくなり売上が低迷しました。
                そこで、私は当時マスク無しで仕事ができず、生産活動を止めざる負えないお客様が多くいらっしゃいました。そこで、マスクを生産し供給することで、お客様が生産活動を再開できるのではないかと考えました。さっそく上司へ提案を行い、中国にも弊社の営業所はありますので至急マスクを生産できるところをチームメンバーで探しました。すると、1社がその案件に乗り出しマスクを生産することになりました。私が中心となって中国の営業所と連携し、価格・製造量・輸入のタイミングなど細かく調整を行い、企画から約1ヶ月でマスクを2万枚を日本に輸入することができました。
                その結果、弊社のお客様にマスクを販売する事ができ大手企業様を中心に注文が殺到し、1日でマスクを完売させることができました。その結果、お客様はマスク不足を解消でき、会社でも3600万円の売上を上げることができました。
                また、この功績により京都市と南丹市から災害時における物資供給を優先的に行うことができる協定を結部ことにも至りました。
                入社後には、さまざまな観点からどのように課題を解決できるか検討し、社内やクライアントの課題解決に貢献したいと思います。
